// DETECT INTENT – COM PONTUAÇÃO E SUB-INTENT
// ==================================================
export function detectIntent(query) {
    const lower = query.toLowerCase().trim();
    let bestIntent = 'informativo';
    let bestScore = 0;

    for (const [intent, data] of Object.entries(intentMapping)) {
        const score = data.keywords.reduce((acc, kw) => acc + (lower.includes(kw) ? 10 : 0), 0);
        if (score > bestScore) {
            bestScore = score;
            bestIntent = intent;
        }
    }

    return { intent: bestIntent, confidence: Math.min(bestScore, 100) };
}

